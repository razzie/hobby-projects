<img src="images/arrow.png" alt="" /> <b>Regisztráció</b><br />
Regisztációhoz kattints a Regisztráció menüpontra. A megjelenõ oldalon minden mezõ kitöltése szükséges.
 Ajánlott egy valós e-mail cím megadása, mert késõbb ennek segítségével lehet lekérni egy újabb jelszót.
 Errõl bõvebben: <u>Elfelejtett jelszó</u>. A megadott e-mail címet harmadik fél számára nem adjuk ki,
 a jelszót pedig kódolt formában tároljuk.
<br /><br />
<img src="images/arrow.png" alt="" /> <b>Elfelejtett jelszó</b><br />
Amikor egy felhasználó elfelejti a jelszavát fontos segítség lehet az Elfelejtett jelszó menüpont.
 A mezõ kitöltése után a rendszer a megadott felhasználó beállított e-mail címére üzenetet küld egy
 4 számjegyû generált jelszóval és egy aktiváló linkkel. A jelszó aktiválása után a felhasználó bejelentkezhet
 az új jelszavával. Ezt bejelentkezés után érdemes megváltoztatni. (Lásd: <u>Beállítások</u>)
<br /><br />
<img src="images/arrow.png" alt="" /> <b>Beállítások</b><br />
A felhasználónak lehetõsége van a regisztrációkor megadott és egyéb adatok megváltoztatására.
 Ezt bejelentkezés után a Beállítások menüpontnál lehet megtenni.
<br /><br />

<img src="images/arrow.png" alt="" /> <b>Szöveg formázása</b><br />
A fórumban és az egyéb helyeken (pl: hírek) formázható a szöveg. A formázandó szöveget a két kód közé kell írni.
 (Használható vegyesen is, például színes és félkövér szöveg.)
<br /><u>Formázás példák</u>:
<br /><br />
#red <font color="#FF0000">Vörös szöveg</font> #/red<br />
#green <font color="#00DD00">Zöld szöveg</font> #/green<br />
#blue <font color="#0000FF">Kék szöveg</font> #/blue<br />
#yellow <font color="#DDDD00">Sárga szöveg</font> #/yellow<br />
#grey <font color="#666666">Szürke szöveg</font> #/grey<br />
<br />
#b <b>Vastag szöveg</b> #/b<br />
#i <i>&nbsp;Dõlt szöveg</i> #/i<br />
#u <u>Aláhúzott szöveg</u> #/u<br />
