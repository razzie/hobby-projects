<p align="justify">
<b>Tisztelt Felhasználók!!</b>
<br />
<br />
A fórum bárkinek lehetõséget nyújt az adott témaköröket érintõ szabad véleménycserére, ám annak érdekében, 
hogy sem a hozzászólók, sem pedig a fórumot üzemeltetõ társaság jogai ne sérüljenek a fórum használata során, 
felhívjuk figyelmedet a következõ szabályokra, melyek minden hozzászólóra egyformán vonatkoznak a fórumon.
<br />
<br />
<br />
<b>A fórum alapelvei</b>
<br />
<menu>
<li>A regisztrációval, illetve a regisztrált nick használatával a felhasználó elfogadja a fórum alapelveit
<li>A fórumot bárki olvashatja, de hozzászólni csak regisztrált tagok tudnak
<li>A fórum moderált
<li>A hozzászólások tartalmáért az azt író hozzászóló felel
<li>A hozzászóló fórumbeli hozzászólásai erejéig lemond szerzõi jogáról
</menu>
<br />
<br />
<b><u>Nem engedélyezettek az alábbiak:</u></b>
<br />
<br /><b>Felhasználó név (becenév) választás:</b>
<br />
<menu>
<li>obszcén, vulgáris nevek használata
<li>bármilyen módon faji, vallási, nemzeti, nemzetiségi, nemi, politikai hovatartozást és lelkiismereti szabadságot sértõ nevek használata
</menu>
<br /><b>Flood:</b>
<br />
<menu>
<li>ugyanazon hozzászólás egymás után többszöri bemásolása egy topicba
<li>ugyanazon hozzászólás bemásolása több fórumtémába
<li>hosszú szövegek céltalan bemásolása
<li>minden olyan beavatkozás, ami megváltoztatja a fórum méretét, megjelenését, illetve akadályozza annak rendeltetésszerû használatát
<li>szövegnélküli (üres) vagy értelmetlen hozzászólás
</menu>
<br /><b>A személyeskedés semmilyen formája nem megengedett:</b>
<br /><br />Tilos a fórumon bárkinek - vagy bármely embercsoport tagjának - az emberi méltóságát megsérteni. 
Kifejezetten tilos a nemzeti, etnikai, vallási, kulturális, nemi és faji alapú elõítéleteknek, 
és megkülönböztetéseknek - akár burkolt formában is - a fórumon hangot adni. 
A fenti szabály megszegése akár azonnali kizárást eredményezhet.
<br /><br />
Tilos bármilyen illegális tevékenységre, illetve a Büntetõ Törvénykönyv hatálya alá esõ cselekedetekre felbújtó, 
ilyen tevékenységet ismertetõ, részletesen leíró, illetve ehhez segítséget kérõ, 
továbbá nyílt szexualitást leíró/ábrázoló hozzászólások, képek elküldése. 
Az ilyen szöveges/képi információt tartalmazó internetes oldalak hivatkozásainak bemutatása is tiltott.
<br /><br />Továbbá Galéria opcióknál kizárólag Flyff-el kapcsolatos képeket tartalmazhat.
<br />
<br /><b>A moderáció, a moderátor</b>
<br />
<menu>
<li>a moderátor elsõdleges feladata (minden moderációs szabály alapja) a normális, emberi hangnemû beszélgetés lehetõségének, körülményeinek fenntartása
<li>a moderátorok a szerkesztõség felkérésére, társadalmi munkában, a fórum szabályzatának megfelelõen végzik feladatukat 
</menu>
<br /><b>Moderátori szankciók:</b>
<br />
<menu>
<li>törlés: a fórum alapelveit sértõ hozzászólások, topicok törlésre kerülnek
<li>javítás: a kifogásolható rész törlésre kerül a hozzászólásból, a topic címe megváltozik
<li>áthelyezés: a moderátorok azokat a topicokat, amelyeket véleményük szerint nem a topic tartalmának megfelelõ rovatban nyitottak, áthelyezik a megfelelõ helyre
<li>hozzászóló letiltása
</menu>
<br /><b>Moderálással kapcsolatos kérdések:</b>
<br />
A moderátori döntéssel kapcsolatosan indított vitának nem a Fórumon van a helye. 
A moderálással kapcsolatos kérdéseket a moderátorokkal lehet megvitatni magánlevélben. 
Amennyiben úgy érzed, hogy jogtalanul moderáltak szintén magánlevélben jelezd a moderátorok felé! 
